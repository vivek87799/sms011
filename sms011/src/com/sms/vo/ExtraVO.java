package com.sms.vo;

public class ExtraVO {
	private String extra_code;
	private String extra_name;
	private int std;
	private String select_type;	
	
	public String getExtra_code() {
		return extra_code;
	}
	public void setExtra_code(String extra_code) {
		this.extra_code = extra_code;
	}
	public String getExtra_name() {
		return extra_name;
	}
	public void setExtra_name(String extra_name) {
		this.extra_name = extra_name;
	}
	public int getStd() {
		return std;
	}
	public void setStd(int std) {
		this.std = std;
	}
	public String getSelect_type() {
		return select_type;
	}
	public void setSelect_type(String select_type) {
		this.select_type = select_type;
	}
	
	
}
